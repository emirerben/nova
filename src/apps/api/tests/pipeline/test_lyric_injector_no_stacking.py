"""No-stacked-text invariant tests for the dynamic crossfade scheduler.

Two product invariants are enforced (see plan §5):

  Level 1 — no double-bright overlap (α > 0.85 on two overlays simultaneously)
  Level 2 — no stacked READABLE same-slot text (α > 0.65 on two overlays in
            the same visual slot simultaneously)

A fix that solves Level 2 solves Level 1 for free; the reverse is not true.

These tests replay the actual Skia alpha curves (`_lyric_line_alpha`) over
fixtures generated by `inject_lyric_overlays`. They were authored to FAIL on
pre-fix `main` (the Mirea bug). On the patched scheduler + renderer pair,
all of them must pass.

The unit-partition test scopes tightly — applies ONLY to pairs whose final
§1d decision is "crossfade" (sqrt curve + matched durations + actual
emitted overlap equals the matched duration). Sparse pairs, hard-cut
fallbacks, solo-demoted reconciliation results, and override-skipped
pairs are out of scope; Level 1/Level 2 cover those instead.
"""

from __future__ import annotations

import json
from itertools import combinations
from pathlib import Path

import pytest

from app.config import settings
from app.pipeline.lyric_injector import apply_lyric_line_overrides, inject_lyric_overlays
from app.pipeline.text_overlay_skia import _lyric_line_alpha

DOUBLE_BRIGHT_ALPHA = 0.85  # Level 1
READABLE_ALPHA = 0.65  # Level 2
MIN_MIDDLE_PEAK_HOLD_MS = 80

_FRAME_S = 1.0 / 30.0
_FIXTURE_DIR = Path(__file__).resolve().parents[1] / "fixtures" / "lyric_no_stacking"


# ──────────────────────────────────────────────────────────────────────────
# Helpers
# ──────────────────────────────────────────────────────────────────────────


def _cache(lines: list[tuple[str, float, float]]) -> dict:
    return {
        # Injector requires a publishable source after 2026-05-27 — see
        # `_INJECTOR_ALLOWED_SOURCES` in app/pipeline/lyric_injector.py.
        "source": "lrclib_synced+whisper",
        "lines": [
            {
                "text": text,
                "start_s": start,
                "end_s": end,
                "words": [{"text": text, "start_s": start, "end_s": end}],
            }
            for text, start, end in lines
        ],
    }


def _recipe(slot_durations_s: tuple[float, ...] = (20.0,)) -> dict:
    return {
        "slots": [
            {"position": i + 1, "target_duration_s": d, "text_overlays": []}
            for i, d in enumerate(slot_durations_s)
        ]
    }


def _inject(
    lines: list[tuple[str, float, float]],
    cfg_extra: dict | None = None,
    slot_durations_s: tuple[float, ...] = (20.0,),
) -> list[dict]:
    cfg = {"enabled": True, "style": "line"}
    if cfg_extra:
        cfg.update(cfg_extra)
    span_start = min(s for _, s, _ in lines) - 1.0 if lines else 0.0
    span_end = max(e for _, _, e in lines) + 1.0 if lines else sum(slot_durations_s)
    out = inject_lyric_overlays(_recipe(slot_durations_s), _cache(lines), span_start, span_end, cfg)
    return [
        o
        for slot in out["slots"]
        for o in slot.get("text_overlays", [])
        if o.get("effect") == "lyric-line"
    ]


def _alpha_at(overlay: dict, t: float) -> float:
    """Replay the actual Skia alpha curve for one overlay at absolute time t.

    `t` is in the same coordinate space as overlay["start_s"]/"end_s"
    (slot-relative seconds — the scheduler emits these).
    """
    s, e = overlay["start_s"], overlay["end_s"]
    if t < s or t > e:
        return 0.0
    return _lyric_line_alpha(overlay, t - s, e - s)


def _same_visual_slot(o1: dict, o2: dict) -> bool:
    """Two overlays share a visual slot iff they would render in the same
    on-screen position (same y_frac, same text_anchor). lyric-line overlays
    all share the same default slot by design; the helper handles the
    general case so a future overlay-style change can't silently bypass."""
    return o1.get("position_y_frac") == o2.get("position_y_frac") and o1.get(
        "text_anchor", "center"
    ) == o2.get("text_anchor", "center")


def _event_boundary_times(overlays: list[dict]) -> list[float]:
    """Yield every event boundary (start, fade-in end, fade-out start, end,
    midpoint) plus ±one frame around each, plus a coarse 5 ms fill grid."""
    seen: set[float] = set()
    if not overlays:
        return []
    for o in overlays:
        s, e = o["start_s"], o["end_s"]
        fade_in_end = s + (o.get("fade_in_ms", 0) / 1000.0)
        fade_out_start = e - (o.get("fade_out_ms", 0) / 1000.0)
        for t in (s, fade_in_end, (s + e) / 2.0, fade_out_start, e):
            for delta in (-_FRAME_S, 0.0, _FRAME_S):
                seen.add(round(t + delta, 6))
    lo = min(o["start_s"] for o in overlays) - 0.05
    hi = max(o["end_s"] for o in overlays) + 0.05
    t = lo
    while t < hi:
        seen.add(round(t, 6))
        t += 0.005
    return sorted(seen)


def _active_alphas(overlays: list[dict], t: float) -> list[tuple[dict, float]]:
    return [(o, _alpha_at(o, t)) for o in overlays if _alpha_at(o, t) > 0]


# ──────────────────────────────────────────────────────────────────────────
# Level 1 / Level 2 — the user-visible invariants
# ──────────────────────────────────────────────────────────────────────────


def test_level1_no_double_bright_pair_anywhere() -> None:
    """No t may have any pair of active lyric overlays both at α > 0.85.
    Pre-fix Mirea fails this around 12.75–12.85 s (slot-relative ~1.25–1.35)."""
    overlays = _inject(
        [
            ("this year we've had to lose", 12.4, 13.0),
            ("our space we've lost", 13.1, 14.3),
        ]
    )
    for t in _event_boundary_times(overlays):
        active = _active_alphas(overlays, t)
        for (_o1, a1), (_o2, a2) in combinations(active, 2):
            assert not (a1 > DOUBLE_BRIGHT_ALPHA and a2 > DOUBLE_BRIGHT_ALPHA), (
                f"L1 double-bright at t={t:.3f}s: A={a1:.2f}, B={a2:.2f}"
            )


def test_level2_no_stacked_readable_same_slot_anywhere() -> None:
    """No t may have any same-slot pair of active overlays both at α > 0.65.
    Pre-fix Mirea fails this for ~177 ms; duration-matching-only fix still
    fails for ~137 ms; the curve-switch fix passes."""
    overlays = _inject(
        [
            ("this year we've had to lose", 12.4, 13.0),
            ("our space we've lost", 13.1, 14.3),
        ]
    )
    for t in _event_boundary_times(overlays):
        active = _active_alphas(overlays, t)
        for (o1, a1), (o2, a2) in combinations(active, 2):
            if _same_visual_slot(o1, o2):
                assert not (a1 > READABLE_ALPHA and a2 > READABLE_ALPHA), (
                    f"L2 same-slot readable stacking at t={t:.3f}s: {a1:.2f}, {a2:.2f}"
                )


# ──────────────────────────────────────────────────────────────────────────
# Unit-partition — the mathematical guard against future drift
# ──────────────────────────────────────────────────────────────────────────


def _is_pure_crossfade_pair(a: dict, b: dict) -> bool:
    """A pair is in scope for unit-partition iff all of:
    - a.fade_out_curve == "sqrt"
    - a.fade_out_ms == b.fade_in_ms
    - actual overlap (b.start_s..a.end_s) equals a.fade_out_ms / 1000
    """
    if a.get("fade_out_curve") != "sqrt":
        return False
    if a.get("fade_out_ms") != b.get("fade_in_ms"):
        return False
    overlap_ms = (a["end_s"] - b["start_s"]) * 1000.0
    return abs(overlap_ms - float(a["fade_out_ms"])) < 5.0


def test_crossfade_alpha_sum_is_unit_partition_pure_pairs_only() -> None:
    """For every pure-crossfade pair, α_a(t) + α_b(t) ≈ 1 across the overlap
    window. Mirror-symmetric curves + matched durations + matched window
    make this an algebraic identity, not an empirical observation."""
    overlays = _inject(
        [
            ("this year we've had to lose", 12.4, 13.0),
            ("our space we've lost", 13.1, 14.3),
        ]
    )
    pure_pairs = [(a, b) for a, b in zip(overlays, overlays[1:]) if _is_pure_crossfade_pair(a, b)]
    if not pure_pairs:
        pytest.skip("no pure crossfade pairs in this fixture")
    for a, b in pure_pairs:
        for t in _event_boundary_times([a, b]):
            if a["start_s"] <= t <= a["end_s"] and b["start_s"] <= t <= b["end_s"]:
                a_alpha = _alpha_at(a, t)
                b_alpha = _alpha_at(b, t)
                assert abs((a_alpha + b_alpha) - 1.0) < 0.05, (
                    f"unit-partition violated at t={t:.3f}s: "
                    f"A+B={a_alpha + b_alpha:.3f} (A={a_alpha:.2f}, B={b_alpha:.2f})"
                )


# ──────────────────────────────────────────────────────────────────────────
# Kill-switch ON ⇒ dynamic post-pass ALWAYS fires (plan §F)
#
# Pre-§F policy: cfg containing fade_in_ms or fade_out_ms was treated as
# operator intent and disabled the post-pass. Empirically the "operator
# intent" signal does not exist in production — the admin Test tab UI
# submits every form field with default values on every render, and
# effective_lyrics_config() merges them into the Job row's
# lyrics_config_effective before inject_lyric_overlays sees it. The
# override gate then silently disabled the dynamic post-pass for every
# preview job in the wild — restoring the exact stacking PR #343 was
# supposed to fix.
#
# New contract: while LYRIC_DYNAMIC_CROSSFADE_ENABLED is True, the
# dynamic post-pass fires for every consecutive pair, regardless of what
# cfg contains. The operator's only rollback is the kill switch
# (process-wide flag flip), which IS still byte-identical to pre-fix
# `main` — pinned by test_kill_switch_disabled_reproduces_pre_fix_output.
# ──────────────────────────────────────────────────────────────────────────


@pytest.mark.parametrize(
    "cfg_extra, name",
    [
        ({"fade_in_ms": 175, "fade_out_ms": 175}, "both_ms"),
        ({"fade_in_ms": 175}, "only_in_ms"),
        ({"fade_out_ms": 175}, "only_out_ms"),
        # _s aliases — the legacy form of the same fields, accepted by
        # `_resolve_fade_ms`. Must also be overridden by the post-pass.
        ({"fade_in_s": 0.175, "fade_out_s": 0.175}, "both_s"),
        ({"fade_in_s": 0.175}, "only_in_s"),
        # The 0 case — pre-fix kill-switch idiom. Under §F the post-pass
        # still fires (the kill switch is the proper rollback path), so
        # caller's 0 is overridden.
        ({"fade_in_ms": 0, "fade_out_ms": 0}, "both_zero_ms"),
        ({"fade_in_s": 0.0}, "fade_in_s_zero"),
        # Mixed _ms + _s — _s wins in _resolve_fade_ms, both should still
        # be overridden by the post-pass.
        ({"fade_in_s": 0.2, "fade_out_ms": 100}, "mixed_s_and_ms"),
    ],
)
def test_dynamic_post_pass_fires_regardless_of_explicit_fade_cfg(
    cfg_extra: dict, name: str
) -> None:
    """Caller-set fade values in cfg do NOT disable the dynamic post-pass.
    The matched-window math always wins while the kill switch is on,
    regardless of which fade-cfg permutation the caller submits:
      - fade_in_ms only / fade_out_ms only / both
      - fade_in_s / fade_out_s (legacy aliases)
      - fade_in_ms=0 / fade_out_ms=0 (legacy "kill via cfg" idiom)
      - mixed _ms + _s

    The override gate was the PR #343 regression. There is exactly one
    rollback path: settings.lyric_dynamic_crossfade_enabled = False
    (whole-system, byte-identical to pre-fix). See plan §F."""
    overlays = _inject(
        [
            ("this year we've had to lose", 12.4, 13.0),
            ("our space we've lost", 13.1, 14.3),
        ],
        cfg_extra=cfg_extra,
    )
    got_curve = overlays[0].get("fade_out_curve")
    assert got_curve == "sqrt", (
        f"{name}: post-pass should fire regardless of cfg, got curve={got_curve!r}"
    )
    assert overlays[0]["fade_out_ms"] == overlays[1]["fade_in_ms"], (
        f"{name}: matched-window math must produce equal fade durations"
    )
    # The caller's value is NOT honored when the post-pass fires — that's
    # the new contract. Verify none of the caller's specified values leaked
    # through verbatim. (175 → matched-window value ≠ 175.)
    for key, val in cfg_extra.items():
        if key == "fade_in_ms":
            assert overlays[1]["fade_in_ms"] != val or val == 0, (
                f"{name}: caller's {key}={val} was honored verbatim"
            )
        if key == "fade_out_ms":
            assert overlays[0]["fade_out_ms"] != val or val == 0, (
                f"{name}: caller's {key}={val} was honored verbatim"
            )


