# ──────────────────────────────────────────────────────────────────────────
# Dense three-line — middle line readability + no triple stack
# ──────────────────────────────────────────────────────────────────────────


@pytest.mark.parametrize(
    "lines",
    [
        [("A", 10.00, 10.40), ("B", 10.50, 10.80), ("C", 10.90, 11.30)],
        [("A", 10.00, 10.30), ("B", 10.35, 10.55), ("C", 10.60, 10.90)],
    ],
)
def test_three_line_middle_remains_readable(lines: list[tuple[str, float, float]]) -> None:
    """In dense A → B → C, B has both fade-in (A→B) and fade-out (B→C). Their
    sum must fit B's visible duration with room for a real peak hold. Either
    the short-line outgoing clamp (§1b) or the §1c reconciliation pass
    guarantees this; this test pins it."""
    overlays = _inject(lines)
    assert len(overlays) == 3
    B = overlays[1]
    B_visible_ms = (B["end_s"] - B["start_s"]) * 1000
    fi = int(B.get("fade_in_ms", 0))
    fo = int(B.get("fade_out_ms", 0))
    assert fi + fo <= B_visible_ms - MIN_MIDDLE_PEAK_HOLD_MS + 5, (
        f"middle B fade sum {fi}+{fo}={fi + fo} ms exceeds budget "
        f"{B_visible_ms - MIN_MIDDLE_PEAK_HOLD_MS} ms (visible {B_visible_ms} ms)"
    )
    # B must reach near-peak alpha somewhere in its window.
    B_peak = max(
        (_alpha_at(B, t) for t in _event_boundary_times([B]) if B["start_s"] <= t <= B["end_s"]),
        default=0.0,
    )
    assert B_peak >= 0.95, f"middle B never reaches peak alpha (max {B_peak:.2f})"
    # No three overlays simultaneously readable.
    for t in _event_boundary_times(overlays):
        readable = [o for o, a in _active_alphas(overlays, t) if a > READABLE_ALPHA]
        assert len(readable) <= 1, (
            f"{len(readable)} lyric overlays readable simultaneously at t={t:.3f}s"
        )
    # No same-slot pair simultaneously readable.
    for t in _event_boundary_times(overlays):
        active = _active_alphas(overlays, t)
        for (o1, a1), (o2, a2) in combinations(active, 2):
            if _same_visual_slot(o1, o2):
                assert not (a1 > READABLE_ALPHA and a2 > READABLE_ALPHA), (
                    f"same-slot readable stacking at t={t:.3f}s"
                )


# ──────────────────────────────────────────────────────────────────────────
# Hard-cut re-anchoring must not stale a later pair candidate
# ──────────────────────────────────────────────────────────────────────────


def test_hard_cut_followed_by_pair_does_not_stale_later_candidate() -> None:
    """When A→B is hard_cut or solo_demoted, B.section_start_s is re-anchored
    AFTER pair candidates were computed. The B→C candidate must still
    produce a valid geometry — no readable stacking, B reaches peak alpha,
    B fade sum fits inside B's possibly-newly-shrunk visible window."""
    overlays = _inject(
        [
            ("a!", 12.92, 13.00),  # too short → triggers hard_cut for A→B
            ("bee", 13.05, 13.65),
            ("see", 13.75, 14.30),
        ]
    )
    assert len(overlays) == 3
    B = overlays[1]
    B_visible_ms = (B["end_s"] - B["start_s"]) * 1000
    fi = int(B.get("fade_in_ms", 0))
    fo = int(B.get("fade_out_ms", 0))
    assert fi + fo <= B_visible_ms - MIN_MIDDLE_PEAK_HOLD_MS + 5, (
        f"B fade sum {fi}+{fo} exceeds budget after hard-cut re-anchor"
    )
    B_peak = max(
        (_alpha_at(B, t) for t in _event_boundary_times([B]) if B["start_s"] <= t <= B["end_s"]),
        default=0.0,
    )
    assert B_peak >= 0.95, f"B never reaches peak after re-anchor (max {B_peak:.2f})"
    for t in _event_boundary_times(overlays):
        active = _active_alphas(overlays, t)
        for (o1, a1), (o2, a2) in combinations(active, 2):
            if _same_visual_slot(o1, o2):
                assert not (a1 > READABLE_ALPHA and a2 > READABLE_ALPHA), (
                    f"L2 violated after hard-cut re-anchor at t={t:.3f}s"
                )


