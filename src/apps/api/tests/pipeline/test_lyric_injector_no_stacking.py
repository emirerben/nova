@pytest.mark.parametrize("snapshot_name, lines, slot_durations_s", _KILL_SWITCH_SNAPSHOTS)
def test_kill_switch_disabled_reproduces_pre_fix_output(
    snapshot_name: str,
    lines: list[tuple[str, float, float]],
    slot_durations_s: tuple[float, ...],
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """settings.lyric_dynamic_crossfade_enabled = False must reproduce
    pre-fix scheduler output byte-identically — no `fade_out_curve` key,
    same fade durations, same section starts/ends, same set of keys on
    every overlay. Snapshots committed under
    tests/fixtures/lyric_no_stacking/*_pre_fix.json."""
    monkeypatch.setattr(settings, "lyric_dynamic_crossfade_enabled", False)
    got = _inject(lines, slot_durations_s=slot_durations_s)
    snapshot = json.loads((_FIXTURE_DIR / snapshot_name).read_text())
    expected = snapshot["overlays"]
    assert len(got) == len(expected), (
        f"overlay count drift: got {len(got)} expected {len(expected)}"
    )
    for i, (got_o, want_o) in enumerate(zip(got, expected)):
        assert "fade_out_curve" not in got_o, (
            f"kill-switch leaked fade_out_curve into overlay [{i}]: {got_o.get('fade_out_curve')!r}"
        )
        assert set(got_o.keys()) - {"lyric_line_key"} == set(want_o.keys()), (
            f"kill-switch added/removed keys on overlay [{i}]: "
            f"{set(got_o.keys()) ^ set(want_o.keys())}"
        )
        assert got_o.get("lyric_line_key") == f"L{i}"
        for field in (
            "start_s",
            "end_s",
            "fade_in_ms",
            "fade_out_ms",
            "lyric_line_id",
            "lyric_segment_index",
            "lyric_segment_count",
        ):
            assert got_o.get(field) == want_o.get(field), (
                f"kill-switch drift on overlay [{i}].{field}: "
                f"got {got_o.get(field)!r} want {want_o.get(field)!r}"
            )


@pytest.mark.parametrize("enabled", [True, False])
def test_text_override_does_not_change_line_schedule_geometry(
    enabled: bool,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    monkeypatch.setattr(settings, "lyric_dynamic_crossfade_enabled", enabled)
    lines = [
        ("this year we've had to lose", 12.4, 13.0),
        ("our space we've lost", 13.1, 14.3),
        ("but we still move", 14.5, 15.2),
    ]
